#include <unistd.h>

void	ft_print_comb2(void)
{
	char str[5] = "00 00";

	while (str[0] <= '9')
	{
		while (str[1] <= '9')
		{
			if (((str[0] - '0') * 10 + (str[1] - '0')) >= ((str[3] - '0') * 10 + (str[4] - '0')))
			{
				str[3] = str[0];
				str[4] = str[1];
				str[4]++;
			}
			while (str[3] <= '9')
			{
				while (str[4] <= '9')
				{
					write (1, &str, 5);
					if (!(str[0] == '9' && str[1] == '8' && str[4] == '9'))
						write(1, ", ", 2);
					str[4]++;
				}
				str[4] = '0';
				str[3]++;
			}
			str[3] = '0';
			str[1]++;
		}
		str[1] = '0';
		str[0]++;
	}
}

